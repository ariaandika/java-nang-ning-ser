class Siswa {
    String name;
    String nis;
    int age;

    public void setName(String name) {
        this.name = name;
    }

    public void setNis(String nis) {
        this.nis = nis;
    }

    public void setAge(int age) {
        this.age = age;
    }

    String getName() {
        return name;
    }

    String getNis() {
        return nis;
    }

    int getAge() {
        return age;
    }

}
